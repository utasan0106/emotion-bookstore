/* ============================================================
 * CHAT_TREE — 感情の棚を選んだ後の、もう一段深い対話。
 * バックトラッキング（オウム返し）で、選んだ言葉をそのまま
 * 返すことで「聴いてもらえている」感覚を強める。
 * ============================================================ */
const CHAT_TREE = (function(){
  const tree = {};
  CATEGORIES.forEach(cat=>{
    const kws = CATEGORY_KEYWORDS[cat.id] || [cat.label];
    const wordA = kws[0] || cat.label;
    const wordB = kws[Math.min(3, kws.length - 1)] || cat.label;
    tree[cat.id] = {
      reply: [`『${cat.label}』ですね。${cat.def}　その中でも、今はどちらの言葉が近いですか。`],
      options: [
        { label:`「${wordA}」に近い気がする`, next: cat.id + '_a' },
        { label:`「${wordB}」の方が近いかも`, next: cat.id + '_b' }
      ]
    };
    tree[cat.id + '_a'] = {
      reply: [`「${wordA}」……そうでしたか。その言葉を選んだだけで、少し輪郭がはっきりした気がしませんか。`],
      shelf: cat.id
    };
    tree[cat.id + '_b'] = {
      reply: [`「${wordB}」……なるほど、そちらの方が近いのですね。教えてくれてありがとうございます。`],
      shelf: cat.id
    };
  });
  return tree;
})();

